using Prover.Core.Models;
using Prover.Core.Models.Instruments;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Prover.CommProtocol.Common.Items;

namespace Prover.Core.Extensions
{
    public static class PressureItems
    {
        public static TransducerType GetTransducerType(this Instrument instrument) => (TransducerType)instrument.Items.GetItem(ItemCodes.Pressure.TransducerType).GetNumericValue(instrument.ItemValues);

        public static string PressureUnits(this Instrument instrument) => instrument.Items.GetItem(ItemCodes.Pressure.Units).Description(instrument.ItemValues);

        public static decimal? EvcBasePressure(this Instrument instrument) => instrument.Items.GetItem(ItemCodes.Pressure.Base).NumericValue(instrument.ItemValues);

        public static decimal? EvcAtmosphericPressure(this Instrument instrument) => instrument.Items.GetItem(ItemCodes.Pressure.Atm).GetNumericValue(instrument.ItemValues);

        public static decimal? EvcPressureRange(this Instrument instrument) => instrument.Items.GetItem(ItemCodes.Pressure.Range).GetNumericValue(instrument.ItemValues);

        public static decimal? EvcGasPressure(this Dictionary<int, string> itemValues)
        {
            return itemValues.GetItem(ItemCodes.Pressure.GasPressure);
        }

        public static decimal? EvcPressureFactor(this Dictionary<int, string> itemValues)
        {
            return itemValues.GetItemValue(ItemCodes.Pressure.Factor);
        }

        public static decimal? EvcUnsqrFactor(this Dictionary<int, string> itemValues)
        {
            return itemValues.GetItemValue(ItemCodes.Pressure.UnsqrFactor);
        }
    }
}
